  subroutine Fstring2Cstring_1d(fstr, cstr)
    character(len=*), intent(in) :: fstr(:)
    character(C_CHAR), intent(out) :: cstr(:)
    integer :: clen, sd(1)
    integer :: cinc
    integer :: i, j
#if 1 >= 2
    integer :: k
#endif
#if 1 >= 3
    integer :: m
#endif
#if 1 >= 4
    integer :: n
#endif
#if 1 == 5
    integer :: q
#endif

    cstr = C_NULL_CHAR
    do i=1,1
       sd(i) = size(fstr,i)
    enddo
    cinc = 0
#if 1 == 1
    clen= len(fstr(1))
    do j=1,sd(1)
       do i=1,max(1,len_trim(fstr(j)))
          cstr(i+cinc*clen) = fstr(j)(i:i)
       end do
       cinc = cinc+1
    enddo
